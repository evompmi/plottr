#!/usr/bin/env node
// Bundles `tools/_core/shared-bundle-entry.ts` (theme + i18n) to a single
// IIFE script that every HTML page loads via
// `<script src="tools/shared.bundle.js">`. These primitives ship as
// plain script-tag globals because:
//   - the inline no-FOUC IIFE in each HTML `<head>` needs the `data-theme`
//     attribute (and the `<html lang>` pin) set synchronously before paint,
//   - the modules' event-listener side-effects (BroadcastChannel,
//     `storage`, `visibilitychange`) must run before the SPA's
//     `<script type="module">` (which is implicitly deferred) parses, and
//   - the static landing's inline scripts call `applyStaticI18n` /
//     `setLang` / `getTheme` directly.
//
// IIFE format keeps the bundle a plain script tag (no module / no
// `module.exports`) so `vm.runInThisContext` and a `<script>` element
// both evaluate it the same way. `globalName` collects the entry's named
// exports into `__plottrShared`; the synthetic
// `Object.assign(globalThis, __plottrShared)` footer (appended below)
// makes `getTheme` / `setTheme` / `toggleTheme` / `getLang` / `setLang` /
// `t` / `applyStaticI18n` reachable from inline scripts in `index.html`,
// `benchmark.html` and `privacy.html` without each `_core/*` module having
// to write to `globalThis` itself.

const fs = require("fs");
const path = require("path");
const esbuild = require("esbuild");

const TOOLS_DIR = path.join(__dirname, "..", "tools");
const OUT_FILE = path.join(TOOLS_DIR, "shared.bundle.js");
const ENTRY = path.join(TOOLS_DIR, "_core/shared-bundle-entry.ts");
// Source modules whose edits should retrigger a rebuild in --watch mode.
// `ENTRY` only re-exports, so watching it alone would miss edits to the
// underlying theme / i18n modules.
const WATCH_FILES = [
  ENTRY,
  path.join(TOOLS_DIR, "_core/theme.ts"),
  path.join(TOOLS_DIR, "_core/i18n.ts"),
];

function buildBundle() {
  const result = esbuild.buildSync({
    entryPoints: [ENTRY],
    bundle: true,
    format: "iife",
    globalName: "__plottrShared",
    platform: "browser",
    target: "es2022",
    write: false,
  });
  // The standalone HTML pages (`index.html`, `benchmark.html`,
  // `privacy.html`) load this bundle via a plain `<script>` and reference
  // `getTheme` / `setTheme` / `toggleTheme` / `getLang` / `setLang` / `t` /
  // `applyStaticI18n` from inline `<script>` blocks — there's no module
  // system there to import from. The synthetic
  // `Object.assign(globalThis, ...)` footer copies the IIFE's named exports
  // onto the global object so the inline scripts continue to resolve those
  // names. `_core/theme.ts` / `_core/i18n.ts` themselves ship shim-free
  // (consistent with the other `_core/*` modules); the global surface is
  // reconstructed at bundling time, here.
  const bundle =
    "// AUTO-GENERATED by scripts/build-shared.js — do not edit by hand.\n" +
    "// Source: tools/_core/shared-bundle-entry.ts (theme + i18n, bundled via esbuild --format=iife).\n" +
    "// Regenerate with `npm run build` or `node scripts/build-shared.js`.\n\n" +
    result.outputFiles[0].text +
    "\nObject.assign(globalThis, __plottrShared);\n";

  // Parse the bundle before writing. esbuild output is already
  // syntactically valid (it parsed the TS to produce it), but the
  // prepended header could in principle carry a stray comment delimiter
  // that breaks the script. `new Function(bundle)` parses without
  // executing.
  try {
    new Function(bundle);
  } catch (err) {
    throw new Error(
      "[build-shared] bundled output did not parse — leaving previous tools/shared.bundle.js untouched.\n" +
        "  Underlying error: " +
        err.message
    );
  }

  fs.writeFileSync(OUT_FILE, bundle);
  return { bytes: Buffer.byteLength(bundle) };
}

function rebuildWithLog() {
  const { bytes } = buildBundle();
  const kb = (bytes / 1024).toFixed(1);
  process.stdout.write(
    `[build-shared] _core/shared-bundle-entry.ts → tools/shared.bundle.js (${kb} KB)\n`
  );
}

if (require.main === module) {
  rebuildWithLog();
  if (process.argv.includes("--watch")) {
    let pending = null;
    const onChange = () => {
      if (pending) clearTimeout(pending);
      pending = setTimeout(() => {
        pending = null;
        try {
          rebuildWithLog();
        } catch (err) {
          process.stderr.write(`[build-shared] ${err.message}\n`);
        }
      }, 50);
    };
    for (const f of WATCH_FILES) {
      fs.watch(f, onChange);
    }
    process.stdout.write(`[build-shared] watching ${WATCH_FILES.length} sources for changes…\n`);
  }
}

// FILES is preserved as a no-op export for any callers that still read it
// (none in tree, but historically the per-tool HTMLs and tests referenced
// this list).
const FILES = [];

module.exports = { buildBundle, FILES };
